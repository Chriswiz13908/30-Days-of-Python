#Builtin-function

#gives minimum value
min(20, 30, 40, 50)

#gives maximum value
max(20, 30, 40, 50)

#takes list as na argument and return min
min([20, 30, 40, 50])

#takes list as an argument and returns max
max([20, 30, 40, 50])

#takes only list as an argument and returns the sum
sum([20, 30, 40, 50])

exit()